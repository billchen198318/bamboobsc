package com.netsteadfast.greenstep.vo;

import com.netsteadfast.greenstep.base.model.BaseValueObj;

public class StrategyMapNodesVO extends BaseValueObj implements java.io.Serializable {	
	private static final long serialVersionUID = 6189937143334165881L;
	private String oid;	
	private String masterOid;
	private String id;
	private String text;
	private int positionX;
	private int positionY;	
	
	@Override
	public String getOid() {
		return this.oid;
	}
	
	public void setOid(String oid) {
		this.oid = oid;
	}

	public String getMasterOid() {
		return masterOid;
	}

	public void setMasterOid(String masterOid) {
		this.masterOid = masterOid;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public int getPositionX() {
		return positionX;
	}

	public void setPositionX(int positionX) {
		this.positionX = positionX;
	}

	public int getPositionY() {
		return positionY;
	}

	public void setPositionY(int positionY) {
		this.positionY = positionY;
	}

}
