package com.netsteadfast.greenstep.vo;

import com.netsteadfast.greenstep.base.model.BaseValueObj;

public class SysFormVO extends BaseValueObj implements java.io.Serializable {
	private static final long serialVersionUID = 2436326629589203293L;
	private String oid;
	private String formId;
	private String templateId;
	private String name;
	private String description;
	
	public SysFormVO() {
		
	}

	public SysFormVO(String oid, String formId, String templateId, String name,
			String description) {
		super();
		this.oid = oid;
		this.formId = formId;
		this.templateId = templateId;
		this.name = name;
		this.description = description;
	}

	@Override
	public String getOid() {
		return this.oid;
	}
	
	public void setOid(String oid) {
		this.oid = oid;
	}

	public String getFormId() {
		return formId;
	}

	public void setFormId(String formId) {
		this.formId = formId;
	}

	public String getTemplateId() {
		return templateId;
	}

	public void setTemplateId(String templateId) {
		this.templateId = templateId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

}
